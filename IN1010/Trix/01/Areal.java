class Areal {


    public static void main(String[] args) {

        int bredde = 10;
        int lengde = 20;

        System.out.println("Bredde: " + bredde);
        System.out.println("Lengde: " + lengde);

        int areal = bredde*lengde;

        System.out.println("Areal: " + areal);

    }
}